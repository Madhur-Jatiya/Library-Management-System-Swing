    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        try {
            // TODO add your handling code here:
            prepareStatement = connection.prepareStatement("select * from librarian where username = ?");
            prepareStatement.setString(1, username.getText());
            ResultSet rs = prepareStatement.executeQuery();
            if (rs.next()) {
                name.setText(rs.getString("name"));
                mobile.setText(rs.getString("mobile"));
                email.setText(rs.getString("email"));
                address.setText(rs.getString("address"));
            } else {
                JOptionPane.showMessageDialog(this, "No librarian found");
            }

        } catch (SQLException ex) {
            Logger.getLogger(Update_librarian.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchActionPerformed

