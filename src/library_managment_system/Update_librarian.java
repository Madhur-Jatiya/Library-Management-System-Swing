    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        try {
            // TODO add your handling code here:
            prepareStatement = connection.prepareStatement("update librarian set name = ?, mobile = ?, email = ?, address = ? where username = ?");
            prepareStatement.setString(1, name.getText());
            prepareStatement.setString(2, mobile.getText());
            prepareStatement.setString(3, email.getText());
            prepareStatement.setString(4, address.getText());
            prepareStatement.setString(5, username.getText());
            prepareStatement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Librarian updated successfully");
            Admin_controls admin_controls = new Admin_controls();
            admin_controls.show();
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(Update_librarian.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_updateActionPerformed

