    private void return_bookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_return_bookActionPerformed
        try {
            // TODO add your handling code here:
            preparedStatement = connection.prepareStatement("select * from issue where books_id = ? and student_name = ?");
            preparedStatement.setString(1, Book_id.getText());
            preparedStatement.setString(2, student_name.getText());
            ResultSet rs = preparedStatement.executeQuery();
            if (rs.next()) {
                increaseBookCount();
                preparedStatement = connection.prepareStatement("update issue set return_book = ? where books_id = ? and student_name = ?");
                preparedStatement.setString(1, "done");
                preparedStatement.setString(2, Book_id.getText());
                preparedStatement.setString(3, student_name.getText());
                preparedStatement.executeUpdate();
                Librarian_controls librarian_controls = new Librarian_controls();
                librarian_controls.show();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "You dont issue any book");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Return_book.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_return_bookActionPerformed

