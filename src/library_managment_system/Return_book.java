    private void increaseBookCount() throws SQLException {
        preparedStatement = connection.prepareStatement("select quantity from books where id = ?");
        preparedStatement.setString(1, Book_id.getText());
        ResultSet rs = preparedStatement.executeQuery();
        if (rs.next()) {
            String s = rs.getString("quantity");
            int count = Integer.parseInt(s);
            count = count + 1;

            preparedStatement = connection.prepareStatement("update books set quantity = ? where id = ?");
            preparedStatement.setInt(1, count);
            preparedStatement.setString(2, Book_id.getText());
            preparedStatement.executeUpdate();
        }
    }
